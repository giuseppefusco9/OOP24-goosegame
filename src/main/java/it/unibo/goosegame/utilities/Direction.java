package it.unibo.goosegame.utilities;
/**
 * Enum representing the four possible directions in a grid.
 */
public enum Direction {
    /**
     * Up direction.
     */
    UP,
    /**
     * Down direction.
     */
    DOWN,
    /**
     * Left direction.
     */
    LEFT,
    /**
     * Right direction.
     */
    RIGHT;
}
